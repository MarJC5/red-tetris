class Piece {
    private rotations: number[][][];
    private currentRotation: number;
    private color: string;
    private position: { x: number; y: number };

    constructor(type: keyof typeof BLOCK_SHAPES) {
        if (!BLOCK_SHAPES[type]) {
            throw new Error(`Invalid piece type: ${type}`);
        }

        this.rotations = BLOCK_SHAPES[type];
        this.currentRotation = 0;
        this.color = `var(--color-${type})`;
        this.position = { x: 3, y: 0 }; // Default starting position
    }

    rotate() {
        this.currentRotation = (this.currentRotation + 1) % this.rotations.length;
    }

    moveLeft() {
        this.position.x -= 1;
    }

    moveRight() {
        this.position.x += 1;
    }

    moveDown() {
        this.position.y += 1;
    }

    getShape(): number[][] {
        return this.rotations[this.currentRotation];
    }

    getColor(): string {
        return this.color;
    }

    getPosition(): { x: number; y: number } {
        return this.position;
    }
}

